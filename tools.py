@function_tool()
async def check_system_errors(
    context: RunContext,  # type: ignore
) -> str:
    """
    Return all errors logged by any tool during this session.
    Call this when the user asks things like:
      - 'what errors happened?'
      - 'show me the error log'
      - 'what went wrong?'
      - 'any problems so far?'
    After reporting the errors, always summarise them in plain English
    and offer actionable suggestions to fix each one.
    """
    errors = get_recent_errors(20)
    report = format_errors_for_agent(errors)
    logger.info(f"[ERRLOG] Diagnostics requested — {len(errors)} error(s) found.")
    return report


@function_tool()
async def clear_error_log(
    context: RunContext,  # type: ignore
) -> str:
    """
    Clear the system error log.
    Call this when the user says 'clear errors', 'reset the log', or 'forget the errors'.
    """
    count = len(get_recent_errors(100))
    clear_errors()
    logger.info("[ERRLOG] Error log cleared by user request.")
    return f"Done, sir. {count} error(s) have been cleared from the system log."