@function_tool()
async def check_email_status(
    context: RunContext,  # type: ignore
) -> str:
    """
    Return the full diagnosis of the last email attempt.
    Call this when the user asks why their email failed or says 'check email error'.
    """
    d = _email_diagnostics
    if d["last_status"] is None:
        return "No email has been attempted yet in this session."
    if d["last_status"] == "success":
        return (
            f"Last email sent successfully via Gmail API.\n"
            f"  To: {d['last_recipient']}\n"
            f"  Subject: {d['last_subject']}\n"
            f"  Result: {d['send_result']}"
        )
    lines = [
        "━━━ EMAIL FAILURE DIAGNOSIS ━━━",
        f"Recipient  : {d['last_recipient'] or 'unknown'}",
        f"Subject    : {d['last_subject'] or 'unknown'}",
        "",
        f"Auth (OAuth)  : {d['auth_result'] or 'not attempted'}",
        f"Send (API)    : {d['send_result'] or 'not attempted'}",
        "",
        f"Error: {d['last_error'] or 'none'}",
        "",
        d["last_error_detail"] or "No detail available.",
    ]
    return "\n".join(lines)


