# Weather Tool
# ---------------------------------------------------------------------------

def _fetch_weather_sync(city: str) -> str:
    response = requests.get(f"https://wttr.in/{city}?format=4", timeout=10)
    if response.status_code == 200:
        return response.text.strip()
    return f"Could not retrieve weather for {city} (HTTP {response.status_code})."


@function_tool()
async def get_weather(
    context: RunContext,  # type: ignore
    city: str,
) -> str:
    """Get detailed current weather for a given city including temperature, condition, humidity and wind."""
    try:
        result = await asyncio.to_thread(_fetch_weather_sync, city)
        logger.info(f"[WEATHER] {city}: {result}")
        return result
    except requests.Timeout:
        err = f"Weather request timed out for '{city}'."
        logger.error(f"[WEATHER] {err}")
        log_error(
            source="get_weather",
            error=err,
            detail=traceback.format_exc(),
            suggestion="Check your internet connection. The wttr.in service may be slow — try again in a moment.",
        )
        return f"Sorry, the weather request timed out for {city}. Please try again."
    except Exception as e:
        err = f"Weather lookup failed for '{city}': {e}"
        logger.error(f"[WEATHER] {err}")
        log_error(
            source="get_weather",
            error=err,
            detail=traceback.format_exc(),
            suggestion="Verify the city name is correct. If the error persists, the weather service may be down.",
        )
        return f"An error occurred while retrieving weather for {city}."


