# ---------------------------------------------------------------------------
# Gmail OAuth helper
# ---------------------------------------------------------------------------

def _get_gmail_service():
    """
    Load Gmail OAuth credentials.
    Works in two modes:
      1. Local:  reads token.json from disk (generated by gmail_auth.py)
      2. Cloud:  reads GMAIL_TOKEN_JSON environment variable (set in Railway/Render dashboard)
    """
    import json as _json

    token_json_env = os.getenv("GMAIL_TOKEN_JSON")

    if token_json_env:
        # ── Cloud mode: token loaded from environment variable ────────────
        logger.info("[EMAIL] Loading Gmail token from GMAIL_TOKEN_JSON env var.")
        creds = Credentials.from_authorized_user_info(
            _json.loads(token_json_env), SCOPES
        )
    elif os.path.exists(TOKEN_FILE):
        # ── Local mode: token loaded from token.json file ─────────────────
        logger.info("[EMAIL] Loading Gmail token from token.json file.")
        creds = Credentials.from_authorized_user_file(TOKEN_FILE, SCOPES)
    else:
        raise FileNotFoundError(
            "Gmail token not found. Either:\n"
            "  Local:  run 'python gmail_auth.py' to generate token.json\n"
            "  Cloud:  set GMAIL_TOKEN_JSON env var in your hosting dashboard"
        )

    if not creds.valid:
        if creds.expired and creds.refresh_token:
            logger.info("[EMAIL] Refreshing expired OAuth token...")
            creds.refresh(Request())
            # Save back locally if running with a file
            if os.path.exists(TOKEN_FILE):
                with open(TOKEN_FILE, "w") as f:
                    f.write(creds.to_json())
                logger.info("[EMAIL] Token refreshed and saved to token.json.")
            else:
                logger.info("[EMAIL] Token refreshed (cloud mode — not saved to disk).")
        else:
            raise RuntimeError(
                "Gmail OAuth token is invalid and cannot be refreshed.\n"
                "  Local:  run 'python gmail_auth.py' again\n"
                "  Cloud:  update GMAIL_TOKEN_JSON env var with a fresh token"
            )

    return build("gmail", "v1", credentials=creds)



