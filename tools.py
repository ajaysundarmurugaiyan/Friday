# Web Search Tool
# ---------------------------------------------------------------------------

def _search_web_sync(query: str) -> str:
    """
    Direct DuckDuckGo search using DDGS API (no langchain wrapper).
    Retries up to 3 times with backoff — handles cloud IP rate-limiting.
    """
    import time
    last_error = None
    for attempt in range(1, 4):  # 3 attempts
        try:
            with DDGS() as ddgs:
                results = list(ddgs.text(query, max_results=5))
            if not results:
                return f"No search results found for '{query}'."
            # Format results clearly for the agent to read aloud
            formatted = []
            for i, r in enumerate(results, 1):
                title = r.get("title", "Result")
                body  = r.get("body", "")
                formatted.append(f"{i}. {title}\n   {body}")
            return "\n\n".join(formatted)
        except Exception as e:
            last_error = e
            logger.warning(f"[SEARCH] Attempt {attempt}/3 failed: {e}")
            if attempt < 3:
                time.sleep(2 * attempt)  # 2s, then 4s backoff
    raise last_error  # re-raise after all retries exhausted


@function_tool()
async def search_web(
    context: RunContext,  # type: ignore
    query: str,
) -> str:
    """Search the web using DuckDuckGo for general knowledge, news, or any topic."""
    try:
        results = await asyncio.to_thread(_search_web_sync, query)
        logger.info(f"[SEARCH] '{query}': {results[:200]}...")
        return results
    except Exception as e:
        err = f"Web search failed for '{query}': {e}"
        logger.error(f"[SEARCH] {err}")
        log_error(
            source="search_web",
            error=err,
            detail=traceback.format_exc(),
            suggestion=(
                "DuckDuckGo is rate-limiting this server's IP. "
                "This is common on cloud platforms. Try again in 30 seconds."
            ),
        )
        return f"Sorry, I was unable to search for '{query}' right now. DuckDuckGo may be temporarily blocking this server. Please try again in a moment."


# ---------------------------------------------------------------------------
# Email Tool  (Gmail REST API — HTTPS, no SMTP ports needed)
# ---------------------------------------------------------------------------

def _send_email_sync(
    recipient_list: list[str],
    subject: str,
    plain_message: str,
    cc_email: Optional[str] = None,
) -> str:
    _email_diagnostics["last_recipient"] = ", ".join(recipient_list)
    _email_diagnostics["last_subject"]   = subject
    _email_diagnostics["last_status"]    = "failed"

    # ── Auth ─────────────────────────────────────────────────────────────────
    try:
        service = _get_gmail_service()
        _email_diagnostics["auth_result"] = "success"
        logger.info("[EMAIL] Gmail OAuth OK.")
    except FileNotFoundError as e:
        _email_diagnostics["auth_result"] = f"FAILED: {e}"
        suggestion = (
            "Run 'python gmail_auth.py' in the project folder. "
            "A browser will open — sign in with your Gmail account. "
            "This only needs to be done once."
        )
        log_error("send_email", str(e), traceback.format_exc(), suggestion)
        logger.error(f"[EMAIL] Auth failed: {e}")
        _email_diagnostics["last_error"]        = str(e)
        _email_diagnostics["last_error_detail"] = suggestion
        return "Email failed: Not authenticated. Please run 'python gmail_auth.py' first."
    except Exception as e:
        _email_diagnostics["auth_result"] = f"FAILED: {e}"
        log_error("send_email", f"OAuth error: {e}", traceback.format_exc(),
                  "Run 'python gmail_auth.py' to re-authenticate.")
        logger.error(f"[EMAIL] OAuth error: {e}")
        _email_diagnostics["last_error"] = str(e)
        return f"Email failed: Authentication error — {e}"

    # ── Build message ─────────────────────────────────────────────────────────
    msg = MIMEMultipart("alternative")
    msg["From"]    = "me"
    msg["To"]      = ", ".join(recipient_list)
    msg["Subject"] = subject
    all_recipients = recipient_list.copy()
    if cc_email:
        msg["Cc"] = cc_email
        all_recipients.append(cc_email)

    html_body = f"""
    <html>
      <body style="font-family: Arial, sans-serif; font-size: 15px; color: #222; line-height: 1.6;">
        <div style="max-width: 640px; margin: 0 auto; padding: 24px;">
          {plain_message.replace(chr(10), '<br>')}
        </div>
      </body>
    </html>
    """
    msg.attach(MIMEText(plain_message, "plain", "utf-8"))
    msg.attach(MIMEText(html_body,    "html",  "utf-8"))

    # ── Send ──────────────────────────────────────────────────────────────────
    try:
        raw    = base64.urlsafe_b64encode(msg.as_bytes()).decode()
        result = service.users().messages().send(userId="me", body={"raw": raw}).execute()

        _email_diagnostics["send_result"] = f"success (id: {result.get('id')})"
        _email_diagnostics["last_status"] = "success"
        _email_diagnostics["last_error"]  = None
        logger.info(f"[EMAIL] ✓ Sent to {all_recipients}. ID: {result.get('id')}")
        return f"Email successfully sent to {', '.join(recipient_list)}."

    except HttpError as api_err:
        code   = api_err.resp.status
        detail = str(api_err)
        hint   = _api_error_hint(code)
        _email_diagnostics["send_result"] = f"HTTP {code}"
        _email_diagnostics["last_error"]  = f"Gmail API error {code}"
        log_error(
            "send_email",
            f"Gmail API HTTP {code}: {detail}",
            traceback.format_exc(),
            hint,
        )
        logger.error(f"[EMAIL] API error {code}: {detail}")
        return f"Email failed: Gmail API error {code}. Say 'check email error' for diagnosis."

    except Exception as e:
        log_error("send_email", f"Send failed: {e}", traceback.format_exc(),
                  "Check your Gmail quota and internet connection.")
        logger.error(f"[EMAIL] Send failed: {e}")
        _email_diagnostics["last_error"] = str(e)
        return f"Email failed: {e}"


def _api_error_hint(status_code: int) -> str:
    return {
        400: "Bad request — the email address or message format may be invalid.",
        401: "Unauthorized — token.json may be expired. Run 'python gmail_auth.py' again.",
        403: "Forbidden — Gmail API may not be enabled in Google Cloud Console, or the OAuth scope is wrong.",
        429: "Rate limit hit — you've sent too many emails in a short period. Wait and retry.",
        500: "Google server error — try again in a few seconds.",
    }.get(status_code, "Check the Google Cloud Console error logs for more detail.")


@function_tool()
async def send_email(
    context: RunContext,  # type: ignore
    to_emails: str,
    subject: str,
    message: str,
    cc_email: Optional[str] = None,
) -> str:
    """
    Send an email using Gmail (via HTTPS — works on all networks).

    Args:
        to_emails: Recipient email address(es). Single or comma-separated.
        subject:   Email subject line.
        message:   Email body text.
        cc_email:  Optional CC address.
    """
    _reset_email_diagnostics()
    try:
        recipient_list = [e.strip() for e in to_emails.split(",")]
        logger.info(f"[EMAIL] → To: {recipient_list} | Subject: '{subject}'")
        result = await asyncio.to_thread(
            _send_email_sync, recipient_list, subject, message, cc_email
        )
        logger.info(f"[EMAIL] Tool result: {result}")
        return result
    except Exception as e:
        detail = traceback.format_exc()
        log_error("send_email", f"Critical tool error: {e}", detail,
                  "Restart the agent. If it persists, check error_log.py.")
        logger.error(f"[EMAIL] Critical: {e}\n{detail}")
        return f"A critical error occurred while sending the email: {e}"


